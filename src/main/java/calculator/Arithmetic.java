package calculator;

public class Arithmetic {
    public float SumFraction(float num1, float num2){
        return num1 + num2;
    }

    public float MinusFraction(float num1, float num2){
        return num1 - num2;
    }

    public float DivisionFraction(float num1, float num2){
        return num1 / num2;
    }

    public float MultiplicationFraction(float num1, float num2){
        return num1 * num2;
    }
}
